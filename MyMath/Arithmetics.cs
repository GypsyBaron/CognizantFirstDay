using System;

namespace MyMath
{
    public class Arithmetics
    {
        public int Add(int num1, int num2)
        {
            int sum;
            sum = num1 + num2;
            return sum;
        }
        public int Minus(int num1, int num2)
        {
            int subtract;
            subtract = num1 - num2;
            return subtract;
        }
    }
}